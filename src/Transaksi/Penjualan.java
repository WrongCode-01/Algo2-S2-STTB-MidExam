        public void AutoID() {
        try {
            String sql = "SELECT * FROM transaksi_jual ORDER BY no_transaksi DESC";
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet rs=stm.executeQuery(sql);
            if (rs.next()) {
                String no_transaksi = rs.getString("no_transaksi").substring(2);
                String TJ = "" + (Integer.parseInt(no_transaksi) + 1);
                String No = "";

                if(TJ.length()==1)
                {No = "000";}
                else if(TJ.length()==2)
                {No = "00";}
                else if(TJ.length()==3)
                {No = "0";}
                else if(TJ.length()==4)
                {No = "";}
                NT_TextField_21552011440.setText("TJ" + No + TJ);
            } else {
                NT_TextField_21552011440.setText("TJ0001");
            }
            rs.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
        
