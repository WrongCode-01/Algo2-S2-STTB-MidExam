        private void cariData(String key){
        try{
            DefaultTableModel model = new DefaultTableModel();
            Object[] judul_kolom = {"Tanggal Penjualan", "No Transaksi", "ID Pelanggan", "Nama Pelanggan", "Kode Barang", "Nama Barang", "Stock", "Harga Satuan", "Jumlah Jual", "Total Harga"};
            model=new DefaultTableModel(null,judul_kolom);
            TBL_Penjualan_21552011440.setModel(model);
            
            Connection conn=(Connection)Koneksi.configDB();
            Statement stt=conn.createStatement();
            model.getDataVector().removeAllElements();
            
            String sql = ("SELECT * from transaksi_jual WHERE no_transaksi LIKE '%"+key+"%' OR tanggal_penjualan LIKE '%"+key+"%' OR id_pelanggan LIKE '%"+key+"%' OR nama_pelanggan LIKE '%"+key+"%' OR kode_barang LIKE '%"+key+"%' OR nama_barang LIKE '%"+key+"%'");  
            ResultSet rs = stt.executeQuery(sql);
            while(rs.next()){
                Object[] data={
                    rs.getString("tanggal_penjualan"),
                    rs.getString("no_transaksi"),
                    rs.getString("id_pelanggan"),
                    rs.getString("nama_pelanggan"),
                    rs.getString("kode_barang"),
                    rs.getString("nama_barang"),         
                    rs.getString("stock"),
                    rs.getString("harga_satuan"),
                    rs.getString("jumlah_jual"),
                    rs.getString("total_harga")         
                };
               model.addRow(data);
            }                
        } catch (Exception ex) {
        System.err.println(ex.getMessage());
        }
    }

